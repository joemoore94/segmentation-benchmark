"""Spatial structure of cross-method segmentation/cell-type disagreement.

Given a set of matched cell pairs (from :func:`segbench.compare.match_cells_by_centroid`)
and per-method cell-type labels (from :func:`segbench.compare.cluster_cell_types`),
this module builds a per-cell "disagreement" indicator and tests whether it is
spatially clustered (Moran's I) rather than randomly scattered across the tissue.
"""

from __future__ import annotations

import numpy as np
import pandas as pd
from anndata import AnnData
from numpy.typing import NDArray
from scipy.sparse import csr_matrix
from scipy.spatial import cKDTree


def _knn_weight_matrix(coords: NDArray[np.floating], k: int = 6) -> csr_matrix:
    """Row-standardized k-nearest-neighbor spatial weight matrix (sparse)."""
    n = coords.shape[0]
    tree = cKDTree(coords)
    _, idx = tree.query(coords, k=k + 1)
    idx = idx[:, 1:]  # drop self (first neighbor is always the point itself)

    rows = np.repeat(np.arange(n), k)
    cols = idx.ravel()
    data = np.full(rows.shape, 1.0 / k)
    return csr_matrix((data, (rows, cols)), shape=(n, n))


def morans_i(
    coords: NDArray[np.floating],
    values: NDArray[np.floating],
    k: int = 6,
    w: csr_matrix | None = None,
) -> float:
    """Moran's I spatial autocorrelation statistic for ``values`` at ``coords``.

    Uses a k-nearest-neighbor, row-standardized spatial weight matrix. ``I``
    is near 0 for spatially random values, positive when nearby points have
    similar values (clustering), and negative for checkerboard-like patterns.
    """
    if w is None:
        w = _knn_weight_matrix(coords, k=k)

    n = len(values)
    z = values - values.mean()
    denom = np.sum(z**2)
    if denom == 0:
        return 0.0

    num = n * (z @ (w @ z))
    return float(num / (w.sum() * denom))


def morans_i_permutation_test(
    coords: NDArray[np.floating],
    values: NDArray[np.floating],
    k: int = 6,
    n_perm: int = 999,
    seed: int = 0,
) -> tuple[float, float]:
    """Observed Moran's I and a two-sided permutation-test p-value.

    The null distribution is generated by randomly permuting ``values``
    across ``coords`` (i.e. no spatial structure) ``n_perm`` times. The
    p-value is the fraction of permuted statistics at least as extreme as
    the observed one (with a +1/+1 continuity correction).
    """
    w = _knn_weight_matrix(coords, k=k)
    observed = morans_i(coords, values, k=k, w=w)

    rng = np.random.default_rng(seed)
    perm_stats = np.empty(n_perm)
    for i in range(n_perm):
        perm_stats[i] = morans_i(coords, rng.permutation(values), k=k, w=w)

    p_value = (np.sum(np.abs(perm_stats) >= abs(observed)) + 1) / (n_perm + 1)
    return observed, float(p_value)


def disagreement_table(
    matches: pd.DataFrame,
    labels_a: pd.Series,
    labels_b: pd.Series,
    adata_a: AnnData,
) -> pd.DataFrame:
    """Per-matched-pair cell-type agreement, with spatial coordinates.

    ``matches`` is the output of :func:`segbench.compare.match_cells_by_centroid`
    (columns ``id_a``, ``id_b``, ``distance``). ``labels_a``/``labels_b`` are
    per-cell cluster labels (e.g. from :func:`segbench.compare.cluster_cell_types`),
    indexed by ``adata_a``/``adata_b``'s ``obs_names``. Coordinates are taken
    from ``adata_a.obs[["centroid_x", "centroid_y"]]`` (the two methods'
    matched cells are colocated by construction).

    Returns a copy of ``matches`` restricted to pairs where both cells have a
    label, with added columns ``label_a``, ``label_b``, ``disagree`` (1.0 if
    the labels differ, else 0.0), ``centroid_x``, ``centroid_y``.
    """
    out = matches.copy()
    out["label_a"] = out["id_a"].map(labels_a)
    out["label_b"] = out["id_b"].map(labels_b)
    out = out.dropna(subset=["label_a", "label_b"]).copy()
    # label_a/label_b may be pandas Categoricals with different category sets
    # (e.g. independent Leiden clusterings per method), which can't be compared
    # directly with !=.
    out["disagree"] = (out["label_a"].astype(str) != out["label_b"].astype(str)).astype(float)

    coords = adata_a.obs.loc[out["id_a"], ["centroid_x", "centroid_y"]]
    out["centroid_x"] = coords["centroid_x"].to_numpy()
    out["centroid_y"] = coords["centroid_y"].to_numpy()
    return out.reset_index(drop=True)


def disagreement_spatial_structure(
    disagreement: pd.DataFrame, k: int = 6, n_perm: int = 999, seed: int = 0
) -> dict[str, object]:
    """Test whether ``disagreement["disagree"]`` is spatially structured.

    ``disagreement`` is the output of :func:`disagreement_table`. Returns a
    dict with ``"morans_i"`` and ``"p_value"`` (permutation test, see
    :func:`morans_i_permutation_test`), ``"n_cells"``, and
    ``"disagreement_rate"`` (fraction of matched pairs with differing labels).
    """
    coords = disagreement[["centroid_x", "centroid_y"]].to_numpy()
    values = disagreement["disagree"].to_numpy(dtype=np.float64)

    morans_i_obs, p_value = morans_i_permutation_test(
        coords, values, k=k, n_perm=n_perm, seed=seed
    )
    return {
        "morans_i": morans_i_obs,
        "p_value": p_value,
        "n_cells": len(disagreement),
        "disagreement_rate": float(values.mean()),
    }
